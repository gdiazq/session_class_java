public class TipoBool {
    public static void main(String[] args) {
        boolean verdadero = true;
        boolean falso = false;
        System.out.println("Valor verdadero: " + verdadero);
        System.out.println("Valor falso: " + falso);

        // Operadores logicos
        System.out.println("Operador AND: " + (verdadero && falso));
        System.out.println("Operador OR: " + (verdadero || falso));
        System.out.println("Operador NOT: " + (!verdadero));
        
        
    }
}
